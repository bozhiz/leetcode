class Solution
{
public:
    int uniquePathsWithObstacles(vector<vector<int>> &obstacleGrid)
    {
        int m = obstacleGrid.size(), n = m ? obstacleGrid[0].size() : 0;

        if (m == 0 || n == 0)
            return 0;

        return path(obstacleGrid, m - 1, n - 1);
    }

private:
    int path(vector<vector<int>> &obstacleGrid, int i, int j)
    {
        int m = obstacleGrid.size(), n = m ? obstacleGrid[0].size() : 0;

        if (m == 0 || n == 0)
            return 0;

        if (i < 0 || j < 0 || obstacleGrid[i][j] == 1)
            return 0;
        else if (i == 0 && j == 0)
            return 1;

        return path(obstacleGrid, i - 1, j) + path(obstacleGrid, i, j - 1);
    }
};

